#ifndef ARITH_H
#define ARITH_H

/**
 * @brief sums two integers
 * 
 * @param a first integer
 * @param b second integer
 * @return int sum of the two numbers
 */
int add(int a, int b);

/**
 * @brief subtracts two integers
 * 
 * @param a first integer
 * @param b second integer
 * @return int subtraction of the two numbers
 */
int sub(int a, int b);

/**
 * @brief multiplies two integers
 * 
 * @param a first integer
 * @param b second integer
 * @return int multiplication of the two numbers
 */
int mul(int a, int b);

/**
 * @brief divides two integers
 * 
 * @param a first integer
 * @param b second integer
 * @return int division of the two numbers
 */
int div(int a, int b);

/**
 * @brief modulo operation on two integers
 * 
 * @param a first integer
 * @param b second integer
 * @return int modulo of the two numbers
 */
int mod(int a, int b);

#endif// ARITH_H
